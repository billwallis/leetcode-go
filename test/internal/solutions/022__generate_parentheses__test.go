func TestSplitParentheses(t *testing.T) {
	var tests = []struct {
		s    string
		want []string
	}{
		{s: "", want: []string{}},
		{s: "()", want: []string{"()"}},
		{s: "()()", want: []string{"()", "()"}},
		{s: "(())", want: []string{"(())"}},
		{s: "()(())", want: []string{"()", "(())"}},
	}

	for _, test := range tests {
		got := solutions.SplitParentheses(test.s)
		if !reflect.DeepEqual(got, test.want) {
			t.Fatalf("SplitParentheses(%s) == %v, want %v", test.s, got, test.want)
		}
	}
}

